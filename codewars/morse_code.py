def decodeMorse(morse):
    words = morse.split("   ")
    letters = morse.split(" ")
    text = []
    for word in words:
        if word:
            temp_word = ""
            for letter in word.split(" "):
                if letter:
                    temp_word += MORSE_CODE[letter]
            text.append(temp_word)
    return " ".join(text)
